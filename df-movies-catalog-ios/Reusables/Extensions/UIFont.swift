import UIKit

extension UIFont {

    // MARK: - Bold

    class var primary25Bold: UIFont {
        return UIFont.boldSystemFont(ofSize: 25.0)
    }

    // MARK: - Regular

    class var primary15Regular: UIFont {
        return UIFont.systemFont(ofSize: 15.0)
    }
}
